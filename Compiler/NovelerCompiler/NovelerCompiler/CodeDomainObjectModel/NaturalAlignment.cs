namespace Noveler.Compiler.CodeDomainObjectModel
{
    internal enum NaturalAlignment
    {
        /// <summary>
        /// Indicates that the type naturally aligns on an 8 bit boundary.
        /// </summary>
        Byte,
        /// <summary>
        /// Indicates that the type naturally aligns on a 16 bit boundary.
        /// </summary>
        Short,
        /// <summary>
        /// Indicates that the type naturally aligns on a 32 bit boundary.
        /// </summary>
        Int,
        /// <summary>
        /// Indicates that the type naturally aligns on a 64 bit boundary.
        /// </summary>
        Long
    }
}
